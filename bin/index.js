#!/usr/bin/env node

const tomono = require('../src');

// eslint-disable-next-line no-unused-vars
const argv = require('yargs')
  .command({
    command: 'serve',
    aliases: ['s'],
    desc: 'generate a template jsonc file used to describe how to create a monorepo',
    builder: () => {},
    handler: (argv) => tomono.init(argv),
  })
  .command({
    command: 'create',
    aliases: ['c'],
    desc: 'create a monorepo',
    builder: (yargs) => {
      return yargs
        .default('f', 'repos.jsonc')
        .alias('f', 'file')
        .describe(
          'f',
          'specify the json file used to describe how to create a monorepo, which can be generated by `tomono serve`',
        );
    },
    handler: async (argv) => {
      const { file } = argv;
      const config = await tomono.parse(file);
      tomono.create(config);
    },
  })
  .command({
    command: 'import',
    aliases: ['i'],
    desc: 'import a branch into current branch',
    builder: (yargs) => {
      return yargs
        .default('o', 'origin')
        .default('b', 'master')
        .default('d', 'origin')
        .alias('o', 'origin')
        .alias('b', 'branch')
        .alias('d', 'destination')
        .describe('o', 'the remote repository alias')
        .describe('b', 'the remote repository branch')
        .describe('d', 'the local directory where the remote branch will be imported into');
    },
    handler: (argv) => {
      const { origin, branch, destination } = argv;
      tomono.import({ origin, branch, destination });
    },
  })
  .version()
  .alias('v', 'version')
  .help()
  .alias('h', 'help').argv;
